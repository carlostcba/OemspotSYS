    PerfilID INTEGER NOT NULL,
    ValorID INTEGER NOT NULL,
    Dia INTEGER NOT NULL,
    HoraInicio INTEGER NOT NULL,
    HoraFin INTEGER NOT NULL,
    PRIMARY KEY(PerfilID, ValorID),
    UNIQUE(PerfilID, Dia, HoraInicio, HoraFin)
);






















































































































































































































































































































































































































































































































